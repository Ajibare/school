let visibleImages = 8;
    const gallery = document.getElementById('gallery');
    const loadMoreBtn = document.getElementById('loadMoreBtn');

    // Function to display images
    function displayImages() {
      gallery.innerHTML = ''; // Clear the gallery
      const imagesToShow = image.slice(0, visibleImages);

      imagesToShow.forEach(img => {
        const imgElement = document.createElement('img');
        imgElement.src = img.url;
        imgElement.alt = `Image ${img.id}`;
        gallery.appendChild(imgElement);
      });
    }

    // Function to show spinner when loading more images
    function showSpinner() {
      loadMoreBtn.innerHTML = 'Loading... <span class="spinner"></span>';
    }

    // Function to stop spinner after loading
    function hideSpinner() {
      loadMoreBtn.innerHTML = 'Load More';
    }

    // Load more images on button click
    loadMoreBtn.addEventListener('click', () => {
      showSpinner();

      // Simulate loading delay (for spinner effect)
      setTimeout(() => {
        visibleImages += 8;
        displayImages();
        hideSpinner();

        if (visibleImages >= image.length) {
          loadMoreBtn.style.display = 'none'; // Hide button if no more images to load
        }
      }, 1000); // 1 second delay for spinner
    });

    // Initial display of images
    displayImages();


